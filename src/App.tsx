function App() {
  const { token, setToken } = useToken();

  if (!token) {
    return <SignIn setToken={setToken} />
  }

  return (
    <ThemeProvider theme={theme}>
      <BrowserRouter>
        <Switch>
          <Route exact path="/">
            <Redirect to="/dashboard" />
          </Route>
          <Route exact path={'/dashboard'} component={Dashboard} />
        </Switch>
      </BrowserRouter>
    </ThemeProvider>
  );
}

export default App;
